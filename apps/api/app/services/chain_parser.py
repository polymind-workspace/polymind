def _get_program_instructions(
    tx_json: dict[str, Any],
) -> list[tuple[int, dict[str, Any]]]:
    """Return (instruction_index, instruction) for all instructions targeting PROGRAM_ID.

    The returned ``instruction`` dict has keys:
        - ``programIdIndex``: int
        - ``accounts``: list[int]
        - ``data``: base58 str
    """
    if not tx_json.get("transaction"):
        return []

    message = tx_json["transaction"].get("message", {})
    account_keys = message.get("accountKeys", [])
    instructions = message.get("instructions", [])

    program_id_str = str(PROGRAM_ID)
    polymind_indices = {
        idx for idx, key in enumerate(account_keys) if key == program_id_str
    }

    result: list[tuple[int, dict[str, Any]]] = []
    for ix_index, ix in enumerate(instructions):
        if ix.get("programIdIndex") in polymind_indices:
            result.append((ix_index, ix))
    return result


def _decode_instruction_data(ix: dict[str, Any]) -> bytes:
    """Decode base58 instruction data to raw bytes."""
    try:
        import base58
    except ImportError as exc:
        raise ChainParserError("base58 package is required for instruction decoding") from exc

    data_b58 = ix.get("data", "")
    if not data_b58:
        return b""
    return base58.b58decode(data_b58)


def _match_instruction(discriminator: bytes) -> PolyMindInstruction | None:
    """Map an 8-byte discriminator to a known instruction."""
    for instr, disc in INSTRUCTION_DISCRIMINATORS.items():
        if disc == discriminator:
            return instr
    return None


def _match_event(discriminator: bytes) -> PolyMindEventType | None:
    """Map an 8-byte discriminator to a known event."""
    for evt, disc in EVENT_DISCRIMINATORS.items():
        if disc == discriminator:
            return evt
    return None


def _decode_event_payload(event_type: PolyMindEventType, raw_payload: bytes) -> Any:
    """Decode raw Borsh bytes into a typed payload dataclass.

    TODO: implement real Borsh decoding once the Anchor IDL is stable.
    Until then, return a dataclass with type-appropriate default values.
    """
    payload_cls = EVENT_PAYLOAD_TYPES.get(event_type)
    if payload_cls is None:
        return {"_raw": raw_payload.hex()}

    fields: dict[str, Any] = {}
    for name, f in payload_cls.__dataclass_fields__.items():
        ftype = f.type
        if ftype is int or ftype == "int":
            fields[name] = 0
        elif ftype is bool or ftype == "bool":
            fields[name] = False
        else:
            fields[name] = ""
    return payload_cls(**fields)  # type: ignore[call-arg]


def _parse_program_logs(logs: list[str] | None) -> list[PolyMindEvent]:
    """Scan Solana program logs for Anchor ``Program data:`` events."""
    import base64

    events: list[PolyMindEvent] = []
    if not logs:
        return events

    prefix = "Program data: "
    event_index = 0
    for log in logs or []:
        if not log.startswith(prefix):
            continue
        encoded = log[len(prefix) :].strip()
        try:
            data = base64.b64decode(encoded)
        except Exception:
            continue
        if len(data) < DISCRIMINATOR_LENGTH:
            continue
        discriminator = data[:DISCRIMINATOR_LENGTH]
        raw_payload = data[DISCRIMINATOR_LENGTH:]
        event_type = _match_event(discriminator)
        if event_type is None:
            continue
        payload = _decode_event_payload(event_type, raw_payload)
        events.append(PolyMindEvent(type=event_type, event_index=event_index, payload=payload))
        event_index += 1
    return events


