"""Solana on-chain transaction and event parsing utilities.

This module provides a Python-only way to inspect Solana transactions that
interact with the PolyMind parimutuel program. It is shared by:

- ``app.workers.indexer`` for ingesting on-chain events into PostgreSQL
- API write endpoints (``propose/finalize/void/dispute/claim``) for verifying
  that a submitted signature actually executes the expected instruction.

The concrete instruction discriminators and event layouts below are placeholders
until the Anchor IDL / program source is finalized. The event *payload* schemas
are taken from the OLD Move contract (``smart-contract/parimutuel/sources``) so
that the indexer and API verification can be built before the Solana program is
ready.
"""

