async def fetch_and_parse_transaction(
    signature: str,
    client: SolanaClient | None = None,
) -> ParsedTransaction:
    """Fetch a transaction from Solana RPC and parse PolyMind instructions/events.

    Args:
        signature: the transaction signature to fetch.
        client: optional existing ``SolanaClient``. If omitted, a new client is
            created and closed automatically.

    Raises:
        ChainParserError: if the transaction is not found or failed.
    """
    own_client = client is None
    active_client = SolanaClient() if own_client else client
    try:
        tx_json = await active_client.get_transaction(signature)
    finally:
        if own_client:
            await active_client.close()

    if tx_json is None:
        raise ChainParserError(f"transaction not found: {signature}")

    meta = tx_json.get("meta", {})
    err = meta.get("err")
    if err is not None:
        raise ChainParserError(f"transaction failed: {err}")

    message = tx_json["transaction"]["message"]
    account_keys = message.get("accountKeys", [])
    if not account_keys:
        raise ChainParserError("transaction has no account keys")

    # The fee payer / primary signer is the first account in the message.
    signer = account_keys[0]
    slot = tx_json.get("slot", 0)
    block_time = tx_json.get("blockTime")

    polymind_instructions: list[ParsedInstruction] = []
    for _, ix in _get_program_instructions(tx_json):
        raw_data = _decode_instruction_data(ix)
        if len(raw_data) < DISCRIMINATOR_LENGTH:
            continue
        discriminator = raw_data[:DISCRIMINATOR_LENGTH]
        instr_name = _match_instruction(discriminator)
        if instr_name is None:
            continue
        accounts = [account_keys[i] for i in ix.get("accounts", []) if i < len(account_keys)]
        polymind_instructions.append(
            ParsedInstruction(
                instruction_name=instr_name,
                signer=signer,
                accounts=accounts,
                data={"_raw": raw_data[DISCRIMINATOR_LENGTH:].hex()},
            )
        )

    polymind_events = _parse_program_logs(meta.get("logMessages", []))

    return ParsedTransaction(
        signature=signature,
        slot=slot,
        block_time=block_time,
        success=True,
        signer=signer,
        polymind_instructions=polymind_instructions,
        polymind_events=polymind_events,
    )


def verify_instruction(
    parsed: ParsedTransaction,
    expected_instruction: PolyMindInstruction,
    expected_signer: str | None = None,
) -> ParsedInstruction:
    """Verify that a parsed transaction contains exactly one expected instruction."""
    matches = [
        ix for ix in parsed.polymind_instructions if ix.instruction_name == expected_instruction
    ]
    if not matches:
        raise ChainParserError(
            f"expected instruction {expected_instruction.value} not found in transaction"
        )
    if len(matches) > 1:
        raise ChainParserError(
            f"expected exactly one {expected_instruction.value} instruction, found {len(matches)}"
        )
    ix = matches[0]
    if expected_signer is not None and ix.signer != expected_signer:
        raise ChainParserError(f"expected signer {expected_signer}, got {ix.signer}")
    return ix


def verify_event(
    parsed: ParsedTransaction,
    expected_event_type: PolyMindEventType,
) -> PolyMindEvent:
    """Verify that a parsed transaction emits exactly one expected event."""
    matches = [ev for ev in parsed.polymind_events if ev.type == expected_event_type]
    if not matches:
        raise ChainParserError(f"expected event {expected_event_type.value} not found in logs")
    if len(matches) > 1:
        raise ChainParserError(
            f"expected exactly one {expected_event_type.value} event, found {len(matches)}"
        )
    return matches[0]


