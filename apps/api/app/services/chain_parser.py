async def verify_creator_action(
    signature: str,
    expected_instruction: PolyMindInstruction,
    creator_address: str,
) -> ParsedInstruction:
    """Verify a creator-only action (propose/finalize/void)."""
    parsed = await fetch_and_parse_transaction(signature)
    return verify_instruction(parsed, expected_instruction, expected_signer=creator_address)


async def verify_admin_action(
    signature: str,
    expected_instruction: PolyMindInstruction,
    admin_address: str,
) -> ParsedInstruction:
    """Verify an admin action (admin_finalize/admin_resolve)."""
    parsed = await fetch_and_parse_transaction(signature)
    return verify_instruction(parsed, expected_instruction, expected_signer=admin_address)


async def verify_dispute_bond(
    signature: str,
    expected_disputer: str,
) -> PolyMindEvent:
    """Verify a dispute bond deposit transaction.

    Returns the ``BondDeposited`` event payload. The caller should further
    validate the event's ``event_id`` / ``market_idx`` / ``disputer`` fields
    once the Borsh schema is known.
    """
    parsed = await fetch_and_parse_transaction(signature)
    verify_instruction(parsed, PolyMindInstruction.FILE_DISPUTE, expected_signer=expected_disputer)
    return verify_event(parsed, PolyMindEventType.BOND_DEPOSITED)


async def verify_claim(
    signature: str,
    expected_claimer: str,
) -> ParsedInstruction:
    """Verify a claim transaction."""
    parsed = await fetch_and_parse_transaction(signature)
    return verify_instruction(parsed, PolyMindInstruction.CLAIM, expected_signer=expected_claimer)


async def verify_trade(
    signature: str,
) -> ParsedInstruction:
    """Verify a bet/swap transaction.

    The caller should compare the bettor address with the user who called sync.
    """
    parsed = await fetch_and_parse_transaction(signature)
    return verify_instruction(parsed, PolyMindInstruction.BET)
