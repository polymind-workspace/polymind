@dataclass(frozen=True)
class ParsedInstruction:
    """A PolyMind program instruction found inside a transaction."""

    instruction_name: PolyMindInstruction
    signer: str
    accounts: list[str]
    data: dict[str, Any]


@dataclass(frozen=True)
class PolyMindEvent:
    """A parsed PolyMind program event."""

    type: PolyMindEventType
    event_index: int
    payload: Any  # one of the *Payload dataclasses above


@dataclass(frozen=True)
class ParsedTransaction:
    """Result of parsing a full on-chain transaction."""

    signature: str
    slot: int
    block_time: int | None
    success: bool
    signer: str
    polymind_instructions: list[ParsedInstruction]
    polymind_events: list[PolyMindEvent]


class ChainParserError(Exception):
    """Raised when a transaction cannot be parsed or verified."""


